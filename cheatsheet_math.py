# Modul math
import math

# Druhá odmocnina
print("Odmocnina z 16:", math.sqrt(16))

# Zaokrouhlení dolů
print("Zaokrouhlení dolů:", math.floor(3.9))

# Zaokrouhlení nahoru
print("Zaokrouhlení nahoru:", math.ceil(3.1))

# Absolutní hodnota
print("Absolutní hodnota:", abs(-5))

# Faktoriál
print("Faktoriál čísla 5:", math.factorial(5))

# Goniometrie
print("Kosinus 90° (v radiánech):", math.cos(math.radians(90)))
